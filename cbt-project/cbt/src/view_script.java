    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Save scores

        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            // Check if the table is empty
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(
                        this, "The table is empty. There is nothing to save.",
                        "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Set the default name and extension for the file
            jFileChooser1.setSelectedFile(new File("output.csv"));

            int userSelection = jFileChooser1.showSaveDialog(this);

            if (userSelection == JFileChooser.APPROVE_OPTION) {
                File fileToSave = jFileChooser1.getSelectedFile();

                // Create a FileWriter
                FileWriter csvWriter = new FileWriter(fileToSave);

                // Write column headers
                for (int i = 0; i < jTable1.getColumnCount(); i++) {
                    csvWriter.append(jTable1.getColumnName(i));
                    if (i < jTable1.getColumnCount() - 1) {
                        csvWriter.append(",");
                    } else {
                        csvWriter.append("\n");
                    }
                }

                // Write data
                for (int row = 0; row < model.getRowCount(); row++) {
                    for (int col = 0; col < model.getColumnCount(); col++) {
                        csvWriter.append(String.valueOf(model.getValueAt(row, col)));
                        if (col < model.getColumnCount() - 1) {
                            csvWriter.append(",");
                        } else {
                            csvWriter.append("\n");
                        }
                    }
                }

                // Close the FileWriter
                csvWriter.flush();
                csvWriter.close();

                JOptionPane.showMessageDialog(this, "CSV file saved successfully!");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error saving CSV file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Search for Script

        try {

            //Check if the two textfirld are empty
            if (jTextField3.getText().equals("") && jTextField4.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Kindly enter both details");
            }

            String testid = jTextField3.getText();
            String matno = jTextField4.getText();

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306"
                    + "/responsebank", "root", "Ilovesql123%");
            PreparedStatement ps = con.prepareStatement("SELECT name, `s/n`, question, optiona, "
                    + "optionb, optionc, optiond, answer, response "
                    + "FROM scripts WHERE testid = ? AND matno = ?");

            ps.setString(1, testid);
            ps.setString(2, matno);
            ResultSet rs = ps.executeQuery();
            
            if (rs.next()){
                String name = rs.getString("name");
                jTextArea1.append("Name: " + name + "\n\n\n");
            }

            while (rs.next()) {
                
                String number = rs.getString("s/n");
                String question = rs.getString("question");
                String optionA = rs.getString("optiona");
                String optionB = rs.getString("optionb");
                String optionC = rs.getString("optionc");
                String optionD = rs.getString("optiond");
                String answer = rs.getString("answer");
                String response = rs.getString("response");

                // Append the information to the JTextArea
                jTextArea1.append("No. " + number + "\n");
                jTextArea1.append("Question: " + question + "\n");
                jTextArea1.append("Option A: " + optionA + "\n");
                jTextArea1.append("Option B: " + optionB + "\n");
                jTextArea1.append("Option C: " + optionC + "\n");
                jTextArea1.append("Option D: " + optionD + "\n");
                jTextArea1.append("Answer: " + answer + "\n");
                jTextArea1.append("Your Answer: " + response + "\n\n");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

