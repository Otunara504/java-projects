    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Search for scores

        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306"
                    + "/responsebank", "root", "Ilovesql123%");

            ResultSet rs = null;

            // TextField1 = testid
            // TextField2 = matno
            if (!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty()) {
                PreparedStatement ps = con.prepareStatement(
                        "SELECT * FROM scores WHERE matno=? AND testid=?");
                ps.setString(1, jTextField1.getText());
                ps.setString(2, jTextField2.getText());

                rs = ps.executeQuery();

            } else if (!jTextField1.getText().isEmpty()) {
                PreparedStatement ps = con.prepareStatement(
                        "SELECT * FROM scores WHERE testid=?");
                ps.setString(1, jTextField1.getText());

                rs = ps.executeQuery();

            } else if (!jTextField2.getText().isEmpty()) {
                PreparedStatement ps = con.prepareStatement(
                        "SELECT * FROM scores WHERE matno=?");
                ps.setString(1, jTextField2.getText());

                rs = ps.executeQuery();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Kindly enter atleast one detail");
                return;
            }

            // Get metadata for the result set
            int columnCount = rs.getMetaData().getColumnCount();

            // Create a table model to hold the data
            DefaultTableModel tableModel = new DefaultTableModel();

            // Add column names to the table model
            for (int i = 1; i <= columnCount; i++) {
                tableModel.addColumn(rs.getMetaData().getColumnName(i));
            }

            // Add rows to the table model
            while (rs.next()) {
                Object[] row = new Object[columnCount];
                for (int i = 1; i <= columnCount; i++) {
                    row[i - 1] = rs.getObject(i);
                }
                tableModel.addRow(row);
            }

            // Set the table model to the JTable
            jTable1.setModel(tableModel);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

