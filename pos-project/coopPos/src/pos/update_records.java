    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Search

        try {
            String username = jTextField1.getText();

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306"
                    + "/pos", "root", "Ilovesql123%");
            PreparedStatement ps = con.prepareStatement("select * from staffinfo where username=?");

            ps.setString(1, username);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                jTextField3.setText(rs.getString(2));
                jTextField4.setText(rs.getString(3));

                String gender = rs.getString(4);
                if (gender.equalsIgnoreCase("Male")) {
                    jRadioButton1.setSelected(true);
                } else {
                    jRadioButton2.setSelected(true);
                }

                String dateStr = rs.getString(5);
                try {
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    Date date = sdf.parse(dateStr);
                    jDateChooser1.setDate(date);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Error parsing date: " + e.getMessage());
                }

                jTextField5.setText(rs.getString(6));
                jComboBox1.setSelectedItem(rs.getString(7));
                jTextField6.setText(rs.getString(8));

                Blob photo = (Blob) rs.getBlob(9);
                if (photo != null) {
                    try {
                        byte[] imageData = photo.getBytes(1, (int) photo.length());
                        ImageIcon originalIcon = new ImageIcon(imageData);
                        Image originalImage = originalIcon.getImage();
                        int labelWidth = jLabel3.getWidth();
                        int labelHeight = jLabel3.getHeight();
                        Image scaledImage = originalImage.getScaledInstance(labelWidth, labelHeight, Image.SCALE_SMOOTH);
                        ImageIcon scaledIcon = new ImageIcon(scaledImage);
                        jLabel3.setIcon(scaledIcon);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                } else {
                    jLabel3.setIcon(null);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

