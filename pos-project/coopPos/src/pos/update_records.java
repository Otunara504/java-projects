    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Update

        try {
            String username = jTextField1.getText();
            String surname = jTextField3.getText();
            String firstname = jTextField4.getText();
            String gender = null;
            if (jRadioButton1.isSelected()) {
                gender = "Male";
            } else if (jRadioButton2.isSelected()) {
                gender = "Female";
            }
            SimpleDateFormat dat = new SimpleDateFormat("yyyy-MM-dd");
            String dob = dat.format(jDateChooser1.getDate());
            String email = jTextField5.getText();
            String department = jComboBox1.getSelectedItem().toString();
            String phoneno = jTextField6.getText();

            byte[] photo = iconToBytes(jLabel3.getIcon());

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306"
                    + "/pos", "root", "Ilovesql123%");
            PreparedStatement ps = con.prepareStatement("update staffinfo set surname=?, firstname=?,"
                    + " gender=?, dob=?, email=?, department=?, phoneno=?, photo=? where username=?");

            ps.setString(1, surname);
            ps.setString(2, firstname);
            ps.setString(3, gender);
            ps.setString(4, dob);
            ps.setString(5, email);
            ps.setString(6, department);
            ps.setString(7, phoneno);
            ps.setBytes(8, photo);
            ps.setString(9, username);

            int rs = ps.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, surname + "'s record updated");

            jTextField1.setText(null);
            jTextField2.setText(null);
            jTextField3.setText(null);
            jTextField4.setText(null);
            jTextField5.setText(null);
            jTextField6.setText(null);
            buttonGroup1.clearSelection();
            jComboBox1.setSelectedIndex(0);
            jDateChooser1.setDate(null);
            jDateChooser1.updateUI();
            jLabel3.setIcon(null);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

